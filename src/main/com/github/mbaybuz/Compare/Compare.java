package main.com.github.mbaybuz.Compare;

public class Compare {

    public static void main(String[] args) {
        int a = 7, b = 8, c = 7;
        System.out.println(a + " < " + b + " = " + (a < b));
        System.out.println(a + " > " + b + " = " + (a > b));
        System.out.println(a + " <= " + b + " = " + (a <= b));
        System.out.println(a + " >= " + c + " = " + (a >= c));
        System.out.println(a + " == " + c + " = " + (a == c));
        System.out.println(a + " != " + c + " = " + (a != c));
    }
}
